Map::Map() {
  dirt = TextureManger::loadTexture("assets/dirt.png");
  grass = TextureManger::loadTexture("assets/grass.png");
  water = TextureManger::loadTexture("assets/water.png");

  LoadMap(lvl1);

  src.x = src.y = dst.x = dst.y = 0;
  src.h = src.w = dst.h = dst.w = 16;
}

Map::~Map() {

}

void Map::LoadMap(int arr[30][40]) {
  for (int i = 0; i < 30; i++)
    for (int j = 0; j < 40; j++)
      map[i][j] = arr[i][j];
}

void Map::DrawMap() {
  int type = 0;
  for (int i = 0; i < 30; i++) {
    for (int j = 0; j < 40; j++) {
      type = map[i][j];
      dst.x = j * 16;
      dst.y = i * 16;

      switch (type) {
        case 0:
          TextureManger::Draw(water, src, dst);
          break;
        case 1:
          TextureManger::Draw(dirt, src, dst);
          break;
        case 2:
          TextureManger::Draw(grass, src, dst);
          break;
         default:
          break;
      }
    }
  }
}
